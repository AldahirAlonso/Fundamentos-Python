# LAB-5 CONVERTIR MILLAS A KILOMETROS Y CONVERTIR KILOMETROS A MILLAS.
kilometers = 12.25
milles = 7.38
milles_to_kilometers = kilometers / 1.61
kilometers_to_milles = milles / 1.61


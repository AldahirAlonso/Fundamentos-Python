# LAB-6 ENTRADAS Y SALIDAS SIMPLES OPERACIONES MATEMATICAS.
print("Ingresa el valor de a: ")
a = float(input())
print("Ingresa el valor de b: ")
b = float(input())

suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b

print("OPERACION SUMA")
print(a, "+" ,b, "=", suma)
print("OPERACION RESTA")
print(a, "-" ,b, "=", resta)
print("OPERACION MULTIPLICACION")
print(a, "*" ,b, "=", multiplicacion)
print("OPERACION DIVISION")
print(a, "/" ,b, "=", division)

print("\nEso es todo!")